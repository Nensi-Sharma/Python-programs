marks=int(input("Enter the student's marks: "))
if marks>=90:
    print("Wohoo!!! You got an A+, Keep it up!")
elif marks>=80 and marks<90:
    print("Good going! You got an A!")
elif marks>=70 and marks<80:
    print("Alright, you have scored a B+ grade!")
elif marks>=60 and marks<70:
    print("Your grade is B. Can do better!")
elif marks>=50 and marks<60:
    print("Your grade is C+, you need to give more effort")
else:
    print("You haven't qualified for this Exam :( Better luck in the next Semester!")
    
